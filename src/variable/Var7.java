package variable;

public class Var7 {
    public static void main(String[] args) {
        int a = 100; // 정수
        double b = 3.14; // 실수
        boolean c = true; // 불린
        char d = 'A'; // 단일 문자
        String e = "Hello Java"; // 문자열

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);



    }
}
