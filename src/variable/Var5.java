package variable;

public class Var5 {
    public static void main(String[] args) {
        // 변수와 초기화를 각각
        int a;
        a = 1;
        System.out.println(a);

        // 변수와 초기화를 동시에!
        int b = 2;
        System.out.println(b);

        int c = 3, d = 4;
        System.out.println(c);
        System.out.println(d);
    }
}
