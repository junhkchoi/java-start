package variable;

public class Var8 {
    public static void main(String[] args) {
        // 정수
        byte b = 127;
        short s = 32767;
        int i = 2147483647;
        long l = 9223372036854775807L;

        // 실수
        float f = 3.14159265358979f;
        double d = 3.14159265358979;
    }
    // 실무에선 숫자를 다룰때 int, long, double을 주로 씀
    // boolean도 조건문에서 자주쓰고,
    // char도 그냥 String으로 대체해서
    // 메모리 절약하는 거 보다 개발 효율을 높이자!

}
