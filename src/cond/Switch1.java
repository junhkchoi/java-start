package cond;

public class Switch1 {
    public static void main(String[] args) {
        int grade = 1;

        switch (grade) {
            case 1:
                System.out.println("1등급");
                break;
            case 2:
                System.out.println("2등급");
                break;
            case 3:
                System.out.println("3등급");
                break;

        }
    }
}
